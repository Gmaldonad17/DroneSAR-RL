from pyprocgen.settings import DEBUG_MOD


class Position:
    ###############################################################
    ########################## __SLOTS__ ##########################
    ###############################################################
    __slots__ = (
        "_x",
        "_y"
    )

    ###############################################################
    ############################ HINTS ############################
    ###############################################################
    _x: int
    _y: int

    ###############################################################
    ########################### __INIT__ ##########################
    ###############################################################
    def __init__(self, x: int, y: int):
        self.set_x(x)
        self.set_y(y)

    ###############################################################
    ########################### GETTERS ###########################
    ###############################################################
    def get_x(self) -> int:
        return self._x

    def get_y(self) -> int:
        return self._y

    ###############################################################
    ########################### SETTERS ###########################
    ###############################################################
    def set_x(self, x: int) -> None:
        self._x = x

    def set_y(self, y: int) -> None:
        self._y = y

    ###############################################################
    ########################### __STR__ ###########################
    ###############################################################
    def __str__(self) -> str:
        return "x = " + str(self.get_x()) + "; y = " + str(self.get_y())
